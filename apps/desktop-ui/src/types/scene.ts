// World-space polyline for a body edge; the renderer turns this into a
// THREE.Line and raycasts against it for picking.
export interface SceneEdge {
  edgeId: string;
  ownerBodyId: string;
  kind: "line" | "circle" | "curve";
  points: Float32Array;
  isSelected: boolean;
  // True when this edge belongs to a body that has a pending fillet /
  // chamfer feature in the document. Ghost edges stay fully pickable
  // (so the user can add / remove edges from the in-progress
  // operation), but the viewport hides them by default to keep the
  // post-op preview visually clean. Tab toggles them visible.
  isGhost: boolean;
}

// World-space vertex of a body. The renderer materializes these as
// small THREE.Points / sphere meshes and raycasts against them ahead
// of edges so vertices are reliably grabbable.
export interface SceneVertex {
  vertexId: string;
  ownerBodyId: string;
  position: [number, number, number];
  isSelected: boolean;
}

export interface MeshScenePrimitive {
  kind: "mesh";
  primitiveId: string;
  label: string;
  // Flat triangle list ready for THREE.BufferGeometry, in world space.
  positions: Float32Array;
  normals: Float32Array;
  indices: Uint32Array;
  isSelected: boolean;
}

// Translucent red overlay rendered on top of the booleaned body while
// the user is editing a cut extrude. Mirrors the contextual modeling "you're
// about to remove this volume" feedback. The renderer keeps the
// material non-pickable (the user picks the booleaned body's faces,
// not this overlay).
export interface CutPreviewScene {
  id: string;
  positions: Float32Array;
  normals: Float32Array;
  indices: Uint32Array;
}

export type ScenePrimitive =
  | BoxScenePrimitive
  | CylinderScenePrimitive
  | PolygonExtrudeScenePrimitive
  | MeshScenePrimitive;

export interface ReferencePlaneScene {
  kind: "reference_plane";
  referenceId: string;
  label: string;
  // Origin planes use the three legacy orientations; construction
  // planes use "custom" and rely on `planeFrame` for placement.
  orientation: "xy" | "yz" | "xz" | "custom";
  position: [number, number, number];
  size: [number, number];
  isSelected: boolean;
  isActiveSketchPlane: boolean;
  // World-space frame for construction planes. Null for origin
  // planes (the renderer uses the legacy orientation rotation
  // instead).
  planeFrame: {
    origin: [number, number, number];
    xAxis: [number, number, number];
    yAxis: [number, number, number];
    normal: [number, number, number];
  } | null;
}

export interface ReferenceAxisScene {
  kind: "reference_axis";
  referenceId: string;
  label: string;
  axis: "x" | "y" | "z";
  start: [number, number, number];
  end: [number, number, number];
}

export type SceneReference = ReferencePlaneScene | ReferenceAxisScene;

export interface SketchLineScene {
  // True when this line is a transient preview generated by a
  // tool (Mirror, Pattern, etc.). Renders dashed and translucent;
  // not selectable.
  isPreview: boolean;
  lineId: string;
  startPointId: string;
  endPointId: string;
  planeId: string;
  start: [number, number, number];
  end: [number, number, number];
  isSelected: boolean;
  constraint: ConstraintType | null;
  // Reference-only construction lines render dashed and don't
  // contribute to profile loop detection in the core.
  isConstruction: boolean;
  isProjected: boolean;
}

export interface SketchCircleScene {
  // See `SketchLineScene.isPreview`.
  isPreview: boolean;
  circleId: string;
  planeId: string;
  planeFrame: {
    origin: { x: number; y: number; z: number };
    x_axis: { x: number; y: number; z: number };
    y_axis: { x: number; y: number; z: number };
    normal: { x: number; y: number; z: number };
  } | null;
  center: [number, number, number];
  radius: number;
  isSelected: boolean;
  isConstruction: boolean;
  isProjected: boolean;
}

// 2D arc derived from `ViewportSketchArc`. The renderer turns this
// into a THREE.Line by sampling the arc between `start` and `end`
// around `center` along the stored sweep direction. v1 freezes the
// arc's shape at creation, so endpoint drag is disabled at the
// THREE picking layer; consumers still treat start/end as snap
// targets via the shared SketchPoint graph.
export interface SketchArcScene {
  // See `SketchLineScene.isPreview`.
  isPreview: boolean;
  arcId: string;
  startPointId: string;
  endPointId: string;
  planeId: string;
  planeFrame: {
    origin: { x: number; y: number; z: number };
    x_axis: { x: number; y: number; z: number };
    y_axis: { x: number; y: number; z: number };
    normal: { x: number; y: number; z: number };
  } | null;
  center: [number, number, number];
  radius: number;
  start: [number, number, number];
  end: [number, number, number];
  ccw: boolean;
  isSelected: boolean;
  isConstruction: boolean;
  isProjected: boolean;
}

export interface SketchPointScene {
  // "endpoint" — line / arc endpoint (default).
  // "center" — circle center.
  // "projected" — standalone point placed by the Project tool. The
  // renderer draws these with a small distinguishing crosshair so
  // the user can tell derived geometry from sketched geometry at a
  // glance.
  pointId: string;
  kind: "endpoint" | "center" | "projected";
  position: [number, number, number];
  isFixed: boolean;
  isSelected: boolean;
}

